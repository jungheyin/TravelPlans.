package com.travelplans.plan.model;

public class PlanTimeView {
	
	private Plan plan;
	private Time time;
	private boolean timeCheck;
	
	public Plan getPlan() {
		return plan;
	}
	public void setPlan(Plan plan) {
		this.plan = plan;
	}
	public Time getTime() {
		return time;
	}
	public void setTime(Time time) {
		this.time = time;
	}
	public boolean isTimeCheck() {
		return timeCheck;
	}
	public void setTimeCheck(boolean timeCheck) {
		this.timeCheck = timeCheck;
	}
	
	

}
